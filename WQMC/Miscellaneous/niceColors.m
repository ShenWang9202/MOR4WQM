function out = niceColors(idx, palette)
% Returns the RGB color of the specified indexes
% Palettes available are regular (10-color), light (10-color), and gray
% (5-color)

    if nargin > 0 && ischar(idx)
        palette = idx;
    elseif nargin < 2
        palette = 'regular';
    end

    % Tableau 10 palette
    colors.regular = [ ...
         31, 119, 180; ...
        255, 127,  14; ...
         44, 160,  44; ...
        214,  39,  40; ...
        148, 103, 189; ...
        140,  86,  75; ...
        227, 119, 194; ...
        127, 127, 127; ...
        188, 189,  34; ...
         23, 190, 207; ...
    ] / 255;

    % Tableau 10 Light palette
    colors.light = [ ...
        174, 199, 232; ...
        255, 187, 120; ...
        152, 223, 138; ...
        255, 152, 150; ...
        198, 176, 213; ...
        196, 156, 148; ...
        247, 182, 210; ...
        199, 199, 199; ...
        219, 219, 141; ...
        158, 218, 229; ...
    ] / 255;

    % Gray 5 palette
    colors.gray = [...
         96,  99, 106; ...
        165, 172, 175; ...
         65,  68,  81; ...
        143, 135, 130; ...
        207, 207, 207; ...
    ] / 255;

    out = colors.(palette);

    if nargin > 0 && ~ischar(idx)
        out = out(mod(idx - 1, size(out, 1)) + 1, :);
    end

end
